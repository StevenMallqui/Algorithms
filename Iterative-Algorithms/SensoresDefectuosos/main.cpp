// Resuelve un caso de prueba, leyendo de la entrada la
// configuración, y escribiendo la respuesta
void resuelveCaso() {
  // leer los datos de la entrada
  int N, err, num; cin >> N >> err;
  int cont = N;
  vector<int> data;
  
  for(int i = 0; i < N; i++){
    cin >> num;
    if(num != err)
      data.push_back(num);
    else
      cont--;
  }

  // escribir sol
  cout << cont << '\n';
  for(int a : data)
    cout << a <<' ';

  cout << '\n';
}

